function solution(numbers) {
  // 1. 숫자 배열을 문자열 배열로 변환합니다.
  const answer = numbers
    .map(String)
    // 2. 두 문자열을 합쳤을 때 더 큰 숫자가 되는 순서로 정렬합니다.
    // (b + a)와 (a + b)를 비교하여 내림차순 정렬
    .sort((a, b) => b + a - (a + b))
    // 3. 정렬된 배열을 하나로 합칩니다.
    .join("");

  // 4. 모든 숫자가 0일 경우(예: [0, 0, 0]) "000"이 아닌 "0"을 반환하도록 예외 처리합니다.
  return answer[0] === "0" ? "0" : answer;
}

// Array 인스턴스의 Join() 메서드는 배열의 모든 요소를 쉼표나 지정된 구분 문자열로 구분하여 연결한 새 문자열을 만들어 반환합니다.
// 배열에 항목이 하나만 있는 경우, 해당 항목은 구분 기호를 사용하지 않고 반환됩니다.
